import SwiftUI

extension Color {
    // Quick access to theme colors
    static let appBackground = AppTheme.background
    static let appSecondaryBackground = AppTheme.secondaryBackground
    static let appTertiaryBackground = AppTheme.tertiaryBackground

    static let appAccentPurple = AppTheme.accentPurple
    static let appAccentPink = AppTheme.accentPink
    static let appAccentCyan = AppTheme.accentCyan

    static let appTextPrimary = AppTheme.textPrimary
    static let appTextSecondary = AppTheme.textSecondary
    static let appTextTertiary = AppTheme.textTertiary
}
